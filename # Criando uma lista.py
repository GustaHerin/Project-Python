# Criando uma lista
numeros = [10, 20, 30, 40]

# Acessando elementos (índices começam em 0)
print(numeros[0])   # 10
print(numeros[-1])  # 40 (último elemento)

# Alterando valores
numeros[1] = 25
print(numeros)  # [10, 25, 30, 40]

# Métodos úteis
numeros.append(70)     # adiciona no final
numeros.remove(30)     # remove um valor
print(len(numeros))    # ler o tamanho da lista


