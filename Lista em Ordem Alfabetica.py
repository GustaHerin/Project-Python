# Versão em ordem de A-Z
frutas = ["banana", "maça", "pera", "Abacate"]
print(frutas)

frutas.sort()
print("Aqui está a lista em ordem alfabetica", frutas)

# Versão em ordem de Z-A
frutas = ["banana", "maça", "pera", "Abacate"]
print(frutas)

frutas.sort(reverse=True)
print("Aqui está a lista em ordem alfabetica", frutas)



